Tile_map::Tile_map() {
    wall     = Texture_manager::load_texture("assets/wall.png");
    platform = Texture_manager::load_texture("assets/platform.png");
    sky      = Texture_manager::load_texture("assets/sky.png");

    load_map(level);

    src.x = 0;
    src.y = 0;
    src.h = 32;
    src.w = 32;

    dst.x = 0;
    dst.y = 0;
    dst.h = 32;
    dst.w = 32;
}

Tile_map::~Tile_map() {
    SDL_DestroyTexture(wall);
    SDL_DestroyTexture(platform);
    SDL_DestroyTexture(sky);
}

void Tile_map::load_map(int arr[20][25]) {

    for (int row = 0; row < 20; row++) {
        for (int col = 0; col < 25; col++) {

            map[row][col] = arr[row][col];
        }
    }
}

void Tile_map::draw_map() {

    int tile_type = 0;
    for (int row = 0; row < 20; row++) {

        for (int col = 0; col < 25; col++) {

            tile_type = map[row][col];

            dst.x = col * 32;
            dst.y = row * 32;

            switch (tile_type) {
                case 0: {
                    Texture_manager::draw(sky, src, dst);
                    break;
                }
                case 1: {
                    Texture_manager::draw(wall, src, dst);
                    break;
                }
                case 2: {
                    Texture_manager::draw(platform, src, dst);
                    break;
                }
                default: { break; }
            }
        }
    }
}